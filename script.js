// ===== CUSTOM CURSOR — SEWING NEEDLE WITH THREAD =====
const cursor = document.getElementById('cursor');
if (cursor && window.matchMedia('(hover:hover)').matches) {
    let curX = 0, curY = 0, targetX = 0, targetY = 0;
    let trailCounter = 0;
    let prevX = 0, prevY = 0;

    document.addEventListener('mousemove', (e) => {
        targetX = e.clientX;
        targetY = e.clientY;
    });

    function updateCursor() {
        curX += (targetX - curX) * 0.15;
        curY += (targetY - curY) * 0.15;
        cursor.style.transform = `translate(${curX - 16}px, ${curY - 2}px)`;

        // Thread trail
        const dx = curX - prevX;
        const dy = curY - prevY;
        const speed = Math.sqrt(dx * dx + dy * dy);
        trailCounter++;
        if (trailCounter % 2 === 0 && speed > 1.5) {
            createThread(curX, curY);
        }
        prevX = curX;
        prevY = curY;
        requestAnimationFrame(updateCursor);
    }
    updateCursor();

    function createThread(x, y) {
        const dot = document.createElement('div');
        dot.className = 'cursor-thread';
        dot.style.left = (x - 2) + 'px';
        dot.style.top = (y - 2) + 'px';
        dot.style.width = dot.style.height = (1.5 + Math.random() * 2.5) + 'px';
        document.body.appendChild(dot);
        setTimeout(() => dot.remove(), 800);
    }

    // Hover effect
    const hoverEls = document.querySelectorAll('a, button, input, select, textarea, .product-card, .epoch-card, .lookbook__card, .polaroid');
    hoverEls.forEach(el => {
        el.addEventListener('mouseenter', () => cursor.classList.add('hovering'));
        el.addEventListener('mouseleave', () => cursor.classList.remove('hovering'));
    });

    document.addEventListener('mouseleave', () => { cursor.style.opacity = '0'; });
    document.addEventListener('mouseenter', () => { cursor.style.opacity = '1'; });
}
