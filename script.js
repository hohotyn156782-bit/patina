let cart = [];
const cartCount = document.getElementById('cartCount');
const cartModal = document.getElementById('cartModal');
const cartBody = document.getElementById('cartBody');
const cartFooter = document.getElementById('cartFooter');
const cartTotal = document.getElementById('cartTotal');

document.getElementById('cartBtn').addEventListener('click', () => {
    cartModal.classList.add('open');
    document.body.style.overflow = 'hidden';
});
document.getElementById('cartOverlay').addEventListener('click', closeCart);
document.getElementById('cartClose').addEventListener('click', closeCart);
document.getElementById('cartCheckout').addEventListener('click', () => {
    closeCart();
    document.body.style.overflow = '';
});

function closeCart() {
    cartModal.classList.remove('open');
    document.body.style.overflow = '';
}

document.addEventListener('click', (e) => {
    const btn = e.target.closest('.product-card__cart');
    if (!btn) return;
    const card = btn.closest('.product-card');
    const name = card.dataset.name;
    const price = parseInt(card.dataset.price);
    const existing = cart.find(i => i.name === name);
    if (existing) existing.qty++;
    else cart.push({ name, price, qty: 1 });

    btn.textContent = '✓';
    btn.classList.add('added');
    setTimeout(() => {
        btn.textContent = translations[currentLang].btn_cart;
        btn.classList.remove('added');
    }, 1200);
    renderCart();
});

function renderCart() {
    const t = translations[currentLang];
    cartCount.textContent = cart.reduce((s, i) => s + i.qty, 0);
    cartCount.classList.toggle('visible', cart.length > 0);
    if (!cart.length) {
        cartBody.innerHTML = `<p class="cart-modal__empty">${t.cart_empty}</p>`;
        cartFooter.style.display = 'none';
        return;
    }
    cartFooter.style.display = 'block';
    let total = 0;
    cartBody.innerHTML = cart.map((item, i) => {
        total += item.price * item.qty;
        return `<div class="cart-item"><div class="cart-item__info"><p class="cart-item__name">${item.name}${item.qty > 1 ? ` × ${item.qty}` : ''}</p><p class="cart-item__price">${(item.price * item.qty).toLocaleString()} ₽</p></div><button class="cart-item__remove" data-index="${i}">${t.cart_remove}</button></div>`;
    }).join('');
    cartTotal.textContent = total.toLocaleString() + ' ₽';
    cartBody.querySelectorAll('.cart-item__remove').forEach(btn => {
        btn.addEventListener('click', () => { cart.splice(parseInt(btn.dataset.index), 1); renderCart(); });
    });
}

