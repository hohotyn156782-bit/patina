// ===== TYPEWRITER HERO =====
const heroTitle = document.getElementById('heroTitle');
const phrases = {
    ru: ['Вещи\nс историей', 'Мода\nвне времени', 'Стиль\nиз прошлого'],
    en: ['Clothes\nwith a Story', 'Fashion\nBeyond Time', 'Style\nfrom the Past']
};
let phraseIndex = 0;
let charIndex = 0;
let isDeleting = false;
let typeTimer = null;

function startTypewriter() {
    clearTimeout(typeTimer);
    phraseIndex = 0;
    charIndex = 0;
    isDeleting = false;
    heroTitle.innerHTML = '';
    typewrite();
}

function typewrite() {
    const list = phrases[currentLang];
    const current = list[phraseIndex];
    const display = current.substring(0, charIndex).replace('\n', '<br>');

    heroTitle.innerHTML = display + '<span class="typewriter-cursor"></span>';

    if (!isDeleting && charIndex < current.length) {
        charIndex++;
        typeTimer = setTimeout(typewrite, 70 + Math.random() * 50);
    } else if (!isDeleting && charIndex === current.length) {
        typeTimer = setTimeout(() => { isDeleting = true; typewrite(); }, 2500);
    } else if (isDeleting && charIndex > 0) {
        charIndex--;
        typeTimer = setTimeout(typewrite, 35);
    } else {
        isDeleting = false;
        phraseIndex = (phraseIndex + 1) % list.length;
        typeTimer = setTimeout(typewrite, 500);
    }
}
startTypewriter();

// ===== CATALOG FILTER =====
const filterBtns = document.querySelectorAll('.catalog__filter');
const catalogCards = document.querySelectorAll('#catalogGrid .product-card');

filterBtns.forEach(btn => {
    btn.addEventListener('click', () => {
        filterBtns.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        const filter = btn.dataset.filter;
        catalogCards.forEach(card => {
            const epoch = card.dataset.epoch;
            const type = card.dataset.type;
            const show = filter === 'all' || epoch === filter || type === filter;
            card.classList.toggle('hidden', !show);
        });
    });
});

// Epoch cards → filter catalog
document.querySelectorAll('.epoch-card').forEach(card => {
    card.addEventListener('click', (e) => {
        e.preventDefault();
        const filter = card.dataset.filter;
        filterBtns.forEach(b => b.classList.remove('active'));
        const target = [...filterBtns].find(b => b.dataset.filter === filter);
        if (target) target.classList.add('active');
        catalogCards.forEach(c => {
            c.classList.toggle('hidden', c.dataset.epoch !== filter);
        });
        document.getElementById('catalog').scrollIntoView({ behavior: 'smooth' });
    });
});

